static void test_shake256(void) {
    /* NIST SHAKE256 sample, message "abc", first 32 output bytes. */
    static const uint8_t expected[32] = {
        0x48, 0x33, 0x66, 0x60, 0x13, 0x60, 0xa8, 0x77, 0x1c, 0x68, 0x63,
        0x08, 0x0c, 0xc4, 0x11, 0x4d, 0x8d, 0xb4, 0x45, 0x30, 0xf8, 0xf1,
        0xe1, 0xee, 0x4f, 0x94, 0xea, 0x37, 0xe7, 0x8b, 0x57, 0x39,
    };

    const uint8_t msg[] = "abc";
    uint8_t out[32];
    shake256(out, sizeof(out), msg, 3);

    if (memcmp(out, expected, sizeof(out)) == 0)
        ESP_LOGI(TAG, "SHAKE256 PASS");
    else
        ESP_LOGE(TAG, "SHAKE256 FAIL");
}

void run_fips202_tests(void) {
    ESP_LOGI(TAG, "=== FIPS 202 tests ===");
    test_sha3_256();
    test_sha3_512();
    test_shake256();
    ESP_LOGI(TAG, "=== FIPS 202 tests done ===");
}
