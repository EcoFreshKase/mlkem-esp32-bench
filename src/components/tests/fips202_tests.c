#include <stdint.h>
#include <string.h>
#include <stdio.h>
#include "esp_log.h"
#include "fips202.h"

static const char *TAG = "fips202_test";

static void test_sha3_256(void) {
    /* NIST FIPS 202 Appendix A, example message "abc". */
    static const uint8_t expected[32] = {
        0x3a, 0x98, 0x5d, 0xa7, 0x4f, 0xe2, 0x25, 0xb2, 0x04, 0x5c, 0x17,
        0x2d, 0x6b, 0xd3, 0x90, 0xbd, 0x85, 0x5f, 0x08, 0x6e, 0x3e, 0x9d,
        0x52, 0x5b, 0x46, 0xbf, 0xe2, 0x45, 0x11, 0x43, 0x15, 0x32,
    };

    const uint8_t msg[] = "abc";
    uint8_t out[32];
    sha3_256(out, msg, 3);

    if (memcmp(out, expected, sizeof(out)) == 0)
        ESP_LOGI(TAG, "SHA3-256 PASS");
    else
        ESP_LOGE(TAG, "SHA3-256 FAIL");
}

